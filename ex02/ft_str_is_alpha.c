int	ft_str_is_alpha(char *str)
{
	int	char_counter;

	char_counter = 0;
	while (*str != '\0')
	{
		if (((int)*str > 64 && (int)*str < 91)
			|| ((int)*str > 96 && (int)*str < 123)
			|| ((int)*str == 0))
		{
			char_counter++;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (1);
}
