int	ft_str_is_numeric(char *str)
{
	int	char_counter;

	char_counter = 0;
	while (*str != '\0')
	{
		if ((int)*str > 47 && (int)*str < 58)
		{
			char_counter++;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (1);
}
